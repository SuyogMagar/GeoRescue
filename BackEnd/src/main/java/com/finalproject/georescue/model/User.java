package com.finalproject.georescue.model;

import org.springframework.data.annotation.Id;
import org.springframework.data.mongodb.core.index.Indexed;
import org.springframework.data.mongodb.core.mapping.Document;

@Document(collection = "users") // Lowercase for better convention
public class User {

    @Id
    private String id; // MongoDB unique identifier

    @Indexed(unique = true)
    private String username; // Unique index for username

    @Indexed(unique = true)
    private String email; // Unique index for email

    private String password; // Password will be stored in hashed format

    // Default constructor
    public User() {}

    // Parameterized constructor
    public User(String username, String email, String password) {
        this.username = username;
        this.email = email;
        this.password = password;
    }

    // Getters and Setters
    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
}
